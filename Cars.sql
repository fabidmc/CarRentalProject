#CREATE DATABASE
CREATE DATABASE IF NOT EXISTS CarSystem;

#SELECT THE DATABASE 
USE CarSystem;

#TABLE CARS
CREATE TABLE CAR(
CAR_ID int(5) NOT NULL,
BRAND varchar(50) NOT NULL, 
MODEL varchar(50) NOT NULL, 
CAR_TYPE varchar(50) NOT NULL, 
PRICE double NOT NULL,
PRIMARY KEY(CAR_ID),
UNIQUE KEY CAR_IDUNIQUE(CAR_ID)
) ENGINE = InnoDB;

#TABLE CUSTOMERS 
CREATE TABLE CUSTOMERS(
CUSTOMER_ID int(10) NOT NULL, 
FIRSTNAME varchar(50) NOT NULL, 
LASTNAME varchar(50) NOT NULL, 
MOBILE varchar(50) NOT NULL, 
ADDRESS varchar(100) NOT NULL, 
STATE varchar(50) NOT NULL, 
COUNTRY varchar(50) NOT NULL, 
EMAIL varchar(50) DEFAULT NULL,
PRIMARY KEY(CUSTOMER_ID),
UNIQUE KEY CUSTOMER_IDUNIQUE (CUSTOMER_ID)
) ENGINE = InnoDB;

#TABLE RESERVATIONS
CREATE TABLE RESERVATION(
RESERVATION_ID varchar(3) NOT NULL, 
AMOUNT double NOT NULL, 
PICKUPDATE date NOT NULL, 
RETURNDATE date NOT NULL,
CUSTOMER_ID int(10) NOT NULL, 
CAR_ID int(5) NOT NULL,
PRIMARY KEY(RESERVATION_ID),
UNIQUE KEY RESERVATION_IDUNIQUE (RESERVATION_ID),
CONSTRAINT CAR_ID FOREIGN KEY(CAR_ID) references CAR(CAR_ID),
CONSTRAINT CUSTOMER_ID FOREIGN KEY(CUSTOMER_ID) REFERENCES CUSTOMERS(CUSTOMER_ID) 
) ENGINE = InnoDB;

